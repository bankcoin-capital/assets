// GET /anchor-flow?chainId=8453[&blocks=1800]
//
// REAL external-stable inflow monitor. Decodes every Uniswap-v3 Swap at the
// kUSD anchor pools (kUSD/USDC + kUSD/USDT, core 0.01% + wall 0.05%) over a
// recent block window, so ops can see whether people are actually BUYING kUSD
// with real USDC/USDT — the only metric that matters. (As of 2026-07-08 only
// ~$118 of real external stables had ever flowed in against ~$20M of standing
// kUSD anchor depth; this endpoint measures that flow directly.)
//
// MONEY-TRUTH RULE (CLAUDE.md): every figure is a LIVE chain read. On any RPC
// failure across ALL configured endpoints we return HTTP 503 — never a partial
// window served as complete, never stale-as-fresh (same discipline as /pools
// and /supply/*). Signed amounts are exact int256 values carried as strings;
// the formatted decimals are DERIVED display values computed at the leaf.
// Window caps NEVER silently truncate: if a caller asks for more than the hard
// cap we serve the capped window AND flag windowCapped:true with a note.
//
// Pool universe: /pools.json — the static registry generated by
// scripts/publish_token_assets.sh (live factory.getPool over the anchor pairs).
// We keep only the kUSD/{USDC,USDT,USDT0} anchor pools for the chain; this
// function NEVER resolves pools itself (a poisoned/stale list can't enter at
// the edge). Block timestamps are best-effort: a missing timestamp yields
// timestamp:null on that swap, it does NOT 503 the whole feed.

import {
  rpcPost,
  formatUnits,
  corsPreflight,
  SupplyError,
} from "../supply/_lib.js";

const CACHE_SECONDS = 30;

// keccak256("Swap(address,address,int256,int256,uint160,uint128,int24)")
const SWAP_TOPIC0 =
  "0xc42079f94a6350d7e6235f29174924f928cc2ac818eb64fed8004e115fbcca67";

// External stables kUSD anchors against (USD-referenced legs we count as "in").
const ANCHOR_STABLES = new Set(["USDC", "USDT", "USDT0"]);

// Window defaults: ~1h on Base's ~2s blocks. Hard cap keeps getLogs work and
// timestamp fan-out bounded (10 × 500-block chunks) and inside public-RPC
// limits. Exceeding the cap is served capped + flagged, never silently cut.
const DEFAULT_BLOCKS = 1800;
const MAX_BLOCKS = 5000;
// getLogs chunk size. Base's public RPC caps a single getLogs at a 10,000-block
// RANGE (error -32614) but RATE-LIMITS many sequential requests (429 from the
// Cloudflare edge — verified 2026-07-08: 10×500-block chunks tripped 429 while
// 1 large chunk succeeds). So we chunk to the largest window that stays under
// the range cap: the whole ≤5000-block window becomes ONE getLogs call, and the
// chunk loop still bounds work correctly if MAX_BLOCKS is ever raised past 10k.
const GETLOGS_CHUNK = 9000;
const MAX_BATCH_CALLS = 10; // public RPC JSON-RPC batch cap (see _lib.js)

const CORS_HEADERS = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Methods": "GET, OPTIONS",
  "Access-Control-Allow-Headers": "*",
};

function jsonResponse(obj, status = 200, cacheable = status === 200) {
  return new Response(JSON.stringify(obj, null, 2) + "\n", {
    status,
    headers: {
      ...CORS_HEADERS,
      "Content-Type": "application/json; charset=utf-8",
      "Cache-Control": cacheable
        ? `public, max-age=${CACHE_SECONDS}`
        : "no-store",
      ...(status === 503 ? { "Retry-After": "10" } : {}),
    },
  });
}

// ---------------------------------------------------------------------------
// Decoders (pure — exported so a Node verification harness can replay them
// against real on-chain logs, the AUDITOR view of the three-way reconcile).
// ---------------------------------------------------------------------------

const TWO_255 = 1n << 255n;
const TWO_256 = 1n << 256n;

// Two's-complement int256 from a 64-hex word (no 0x). Uniswap v3 encodes
// amount0/amount1 as SIGNED: positive = token flowed INTO the pool, negative =
// out. This is the load-bearing decode for BUY vs SELL direction.
export function decodeInt256(word64) {
  const v = BigInt("0x" + word64);
  return v >= TWO_255 ? v - TWO_256 : v;
}

// int24 tick decoder (identical to /pools): the ABI sign-extends int24 to the
// full 32-byte word, so a naive full-word signed read yields a value OUTSIDE
// int24 range. Read the low 24 bits and sign-extend from bit 23 — correct
// regardless of upstream padding. e.g. word …fffff9d ⇒ 0xffff9d ⇒ −99.
export function decodeInt24(word64) {
  const low24 = BigInt("0x" + word64.slice(-6));
  return low24 >= 0x800000n ? low24 - 0x1000000n : low24;
}

function topicToAddress(topic) {
  return ("0x" + topic.slice(-40)).toLowerCase();
}

// Decode one Swap log against its anchor-pool descriptor. Returns the fully
// resolved swap record (raw signed strings + derived formatted decimals).
export function decodeSwap(log, pool) {
  const data = (log.data || "").replace(/^0x/, "");
  if (data.length < 5 * 64) {
    throw new Error(
      `Swap data too short for ${pool.pair} (${pool.pool}) tx ${log.transactionHash}`
    );
  }
  const word = (i) => data.slice(i * 64, (i + 1) * 64);
  const amount0 = decodeInt256(word(0)); // token0 delta (signed)
  const amount1 = decodeInt256(word(1)); // token1 delta (signed)
  const tick = decodeInt24(word(4));

  // token0/token1 by lexicographic address order (token0 = smaller lowercased
  // hex). amount0↔token0, amount1↔token1.
  const kusdIsToken0 = pool.kusdAddr < pool.stableAddr;
  const stableAmount = kusdIsToken0 ? amount1 : amount0;
  const kusdAmount = kusdIsToken0 ? amount0 : amount1;

  // Sign convention: stableAmount > 0 ⇒ stable flowed INTO the pool ⇒ someone
  // BOUGHT kUSD with real external stable (the inflow we want). < 0 ⇒ stable
  // left our reserves (kUSD sold back).
  const direction = stableAmount > 0n ? "BUY_kUSD" : "SELL_kUSD";

  return {
    block: Number(BigInt(log.blockNumber)),
    timestamp: null, // filled in later (best-effort)
    tx: log.transactionHash,
    logIndex: Number(BigInt(log.logIndex)),
    pool: pool.pool,
    pair: pool.pair,
    fee: pool.fee,
    direction,
    stableSymbol: pool.stableSymbol,
    stableAmount: formatUnits(stableAmount, pool.stableDecimals),
    stableAmountRaw: stableAmount.toString(),
    kusdAmount: formatUnits(kusdAmount, pool.kusdDecimals),
    kusdAmountRaw: kusdAmount.toString(),
    sender: topicToAddress(log.topics[1]),
    recipient: topicToAddress(log.topics[2]),
    tick: Number(tick),
    // internal (stripped from response): exact signed stable delta for summing
    _stableRaw: stableAmount,
  };
}

