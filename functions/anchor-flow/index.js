async function computeAnchorFlow(context) {
  const url = new URL(context.request.url);
  const chainId = url.searchParams.get("chainId");
  if (!chainId) throw new SupplyError(400, "required query param: chainId");
  if (!/^[0-9]+$/.test(chainId)) {
    throw new SupplyError(400, "chainId must be a decimal integer");
  }

  // Window: default 1800, hard cap MAX_BLOCKS, NEVER silently truncated.
  const blocksParam = url.searchParams.get("blocks");
  let requestedBlocks = DEFAULT_BLOCKS;
  if (blocksParam !== null) {
    if (!/^[0-9]+$/.test(blocksParam) || blocksParam === "0") {
      throw new SupplyError(400, "blocks must be a positive decimal integer");
    }
    requestedBlocks = Number(blocksParam);
  }
  let windowBlocks = requestedBlocks;
  let windowCapped = false;
  let note;
  if (requestedBlocks > MAX_BLOCKS) {
    windowBlocks = MAX_BLOCKS;
    windowCapped = true;
    note =
      `requested blocks=${requestedBlocks} exceeds hard cap ${MAX_BLOCKS}; ` +
      `serving the most recent ${MAX_BLOCKS} blocks only (window NOT silently ` +
      `truncated — see windowCapped). Query a narrower range or page backwards.`;
  }

  const config = await loadPools(context);
  const registry = config[chainId];
  if (!Array.isArray(registry)) {
    const chains = Object.keys(config).filter((k) => /^[0-9]+$/.test(k));
    throw new SupplyError(
      404,
      `no pool registry for chainId ${chainId}; supported: ${chains.join(", ")}`
    );
  }
  const rpcs = (config.rpcs && config.rpcs[chainId]) || [];
  if (!rpcs.length) {
    throw new SupplyError(503, `pool registry missing RPC config for chainId ${chainId}`);
  }

  // Keep only kUSD/{USDC,USDT,USDT0} anchor pools.
  const anchors = registry
    .filter(
      (r) =>
        (r.symA === "kUSD" && ANCHOR_STABLES.has(r.symB)) ||
        (r.symB === "kUSD" && ANCHOR_STABLES.has(r.symA))
    )
    .map(toAnchor);

  const poolAddrs = anchors.map((a) => a.pool);
  const anchorPoolMap = new Map(anchors.map((a) => [a.pool.toLowerCase(), a]));

  const errors = [];
  for (const rpcUrl of rpcs) {
    try {
      const latest = await latestBlock(rpcUrl);
      const toBlock = latest;
      const fromBlock = Math.max(0, toBlock - windowBlocks);

      // No anchor pools on this chain (e.g. Arbitrum today): honest empty feed,
      // still block-pinned, with a note. Not an error.
      if (!poolAddrs.length) {
        return {
          chainId: Number(chainId),
          fromBlock, toBlock, windowBlocks, windowCapped,
          ...(note ? { note } : {}),
          anchorNote: `no kUSD anchor pools registered for chainId ${chainId}`,
          count: 0, swaps: [],
          summary: {
            perPool: [], netStableInUSDC: "0", netStableInUSDCRaw: "0",
            netStableInUSDT: "0", netStableInUSDTRaw: "0",
            netStableInTotal: "0", netStableInTotalRaw: "0",
            netStableInBySymbol: {},
          },
          source: { rpc: rpcUrl, registry: "/pools.json", registryVersion: config.version },
        };
      }

      const rawLogs = await getSwapLogs(rpcUrl, poolAddrs, fromBlock, toBlock);

      const swaps = [];
      for (const log of rawLogs) {
        const anchor = anchorPoolMap.get((log.address || "").toLowerCase());
        if (!anchor) continue; // not one of ours (defensive)
        swaps.push(decodeSwap(log, anchor));
      }
      // Deterministic order: block asc, then logIndex asc.
      swaps.sort((a, b) => a.block - b.block || a.logIndex - b.logIndex);

      // Best-effort timestamps (never 503s the feed).
      const tsMap = await resolveTimestamps(rpcUrl, swaps.map((s) => s.block));
      for (const s of swaps) {
        if (tsMap.has(s.block)) s.timestamp = tsMap.get(s.block);
      }

      const summary = buildSummary(anchors, swaps);

      // Strip the internal BigInt field before serialising.
      const cleanSwaps = swaps.map(({ _stableRaw, ...rest }) => rest);

      return {
        chainId: Number(chainId),
        fromBlock, toBlock, windowBlocks, windowCapped,
        ...(note ? { note } : {}),
        count: cleanSwaps.length,
        swaps: cleanSwaps,
        summary,
        source: {
          rpc: rpcUrl,
          registry: "/pools.json",
          registryVersion: config.version,
          registryGeneratedAtUtc: config.generatedAtUtc,
          swapTopic0: SWAP_TOPIC0,
          note:
            "stableAmount>0 ⇒ BUY_kUSD (real external stable in); <0 ⇒ SELL_kUSD. " +
            "Amounts are exact int256 (raw) with derived 6-decimal display.",
        },
      };
    } catch (e) {
      errors.push(String(e && e.message ? e.message : e));
    }
  }
  throw new SupplyError(
    503,
    `all RPC endpoints failed for chainId ${chainId}: ${errors.join(" | ")}`
  );
}

// 30s edge cache (Cache API). Only 200s cached; errors never cached.
async function withEdgeCache(context, handler) {
  const url = new URL(context.request.url);
  const key = new Request(
    `${url.origin}${url.pathname}?chainId=${
      url.searchParams.get("chainId") || ""
    }&blocks=${url.searchParams.get("blocks") || ""}`,
    { method: "GET" }
  );
  const cache = caches.default;
  const hit = await cache.match(key);
  if (hit) return hit;
  const res = await handler();
  if (res.status === 200) context.waitUntil(cache.put(key, res.clone()));
  return res;
}

export async function onRequestGet(context) {
  return withEdgeCache(context, async () => {
    try {
      return jsonResponse(await computeAnchorFlow(context));
    } catch (e) {
      const status = e instanceof SupplyError ? e.status : 500;
      return jsonResponse(
        { error: String(e && e.message ? e.message : e) },
        status,
        false
      );
    }
  });
}

