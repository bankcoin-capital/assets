async function loadPools(context) {
  const url = new URL("/pools.json", context.request.url);
  const res = await context.env.ASSETS.fetch(new Request(url.toString()));
  if (!res.ok) throw new SupplyError(503, "pool registry (pools.json) unavailable");
  return await res.json();
}

// Normalise a registry row into an anchor descriptor with kUSD/stable roles
// resolved (defensive to either symbol ordering in the registry).
function toAnchor(row) {
  let kusdSym, kusdAddr, kusdDecimals, stableSym, stableAddr, stableDecimals;
  if (row.symA === "kUSD") {
    kusdSym = row.symA; kusdAddr = row.addrA; kusdDecimals = row.decimalsA;
    stableSym = row.symB; stableAddr = row.addrB; stableDecimals = row.decimalsB;
  } else {
    kusdSym = row.symB; kusdAddr = row.addrB; kusdDecimals = row.decimalsB;
    stableSym = row.symA; stableAddr = row.addrA; stableDecimals = row.decimalsA;
  }
  return {
    pair: row.pair,
    fee: row.fee,
    pool: row.pool,
    kusdSym,
    kusdAddr: kusdAddr.toLowerCase(),
    kusdDecimals,
    stableSymbol: stableSym,
    stableAddr: stableAddr.toLowerCase(),
    stableDecimals,
  };
}

// ---------------------------------------------------------------------------
// Chain reads (one RPC endpoint, whole window; caller fails over on throw)
// ---------------------------------------------------------------------------

async function latestBlock(rpcUrl) {
  const r = await rpcPost(rpcUrl, {
    jsonrpc: "2.0", id: 0, method: "eth_blockNumber", params: [],
  });
  if (r.error || !r.result) {
    throw new Error(`${rpcUrl} eth_blockNumber: ${JSON.stringify(r.error)}`);
  }
  return Number(BigInt(r.result));
}

// getLogs for the Swap topic across all anchor pool addresses, chunked to
// GETLOGS_CHUNK-block windows and aggregated. A chunk error throws (⇒ fail
// over to the next RPC ⇒ 503 if all fail) — never a partial window.
async function getSwapLogs(rpcUrl, poolAddrs, fromBlock, toBlock) {
  const logs = [];
  for (let start = fromBlock; start <= toBlock; start += GETLOGS_CHUNK) {
    const end = Math.min(start + GETLOGS_CHUNK - 1, toBlock);
    const r = await rpcPost(rpcUrl, {
      jsonrpc: "2.0", id: 1, method: "eth_getLogs",
      params: [{
        fromBlock: "0x" + start.toString(16),
        toBlock: "0x" + end.toString(16),
        address: poolAddrs,
        topics: [SWAP_TOPIC0],
      }],
    });
    if (r.error) {
      throw new Error(
        `${rpcUrl} eth_getLogs [${start},${end}]: ${JSON.stringify(r.error)}`
      );
    }
    if (!Array.isArray(r.result)) {
      throw new Error(
        `${rpcUrl} eth_getLogs [${start},${end}]: non-array result`
      );
    }
    logs.push(...r.result);
  }
  return logs;
}

// Best-effort block timestamps: dedupe the block numbers we actually have and
// batch eth_getBlockByNumber. On ANY failure we return an empty map (caller
// leaves timestamp:null) — a missing timestamp must never 503 the flow feed.
async function resolveTimestamps(rpcUrl, blockNums) {
  const out = new Map();
  const unique = [...new Set(blockNums)];
  try {
    for (let i = 0; i < unique.length; i += MAX_BATCH_CALLS) {
      const chunk = unique.slice(i, i + MAX_BATCH_CALLS);
      const batch = chunk.map((bn, j) => ({
        jsonrpc: "2.0", id: j,
        method: "eth_getBlockByNumber",
        params: ["0x" + bn.toString(16), false],
      }));
      const res = await rpcPost(rpcUrl, batch);
      if (!Array.isArray(res)) return new Map(); // shape mismatch ⇒ give up quietly
      for (let j = 0; j < res.length; j++) {
        const blk = res[j] && res[j].result;
        if (blk && blk.timestamp) out.set(chunk[j], Number(BigInt(blk.timestamp)));
      }
    }
  } catch {
    return new Map();
  }
  return out;
}

// ---------------------------------------------------------------------------
// Summary (signed BigInt sums — no floats, per CLAUDE.md)
// ---------------------------------------------------------------------------

function buildSummary(anchors, swaps) {
  // Per-pool tallies (every anchor pool listed, even with zero swaps, so ops
  // sees full coverage).
  const perPoolMap = new Map();
  for (const a of anchors) {
    perPoolMap.set(a.pool.toLowerCase(), {
      pool: a.pool, pair: a.pair, fee: a.fee, stableSymbol: a.stableSymbol,
      buys: 0, sells: 0, swaps: 0, _net: 0n, _dec: a.stableDecimals,
    });
  }
  const bySymbol = new Map(); // stableSymbol → signed BigInt sum
  for (const s of swaps) {
    const pp = perPoolMap.get(s.pool.toLowerCase());
    if (pp) {
      pp.swaps += 1;
      if (s.direction === "BUY_kUSD") pp.buys += 1; else pp.sells += 1;
      pp._net += s._stableRaw;
    }
    bySymbol.set(
      s.stableSymbol,
      (bySymbol.get(s.stableSymbol) || 0n) + s._stableRaw
    );
  }

  const perPool = [...perPoolMap.values()].map((p) => ({
    pool: p.pool, pair: p.pair, fee: p.fee, stableSymbol: p.stableSymbol,
    buys: p.buys, sells: p.sells, swaps: p.swaps,
    netStableIn: formatUnits(p._net, p._dec),
    netStableInRaw: p._net.toString(),
  }));

  const usdc = bySymbol.get("USDC") || 0n;
  const usdt = (bySymbol.get("USDT") || 0n) + (bySymbol.get("USDT0") || 0n);
  let total = 0n;
  for (const v of bySymbol.values()) total += v; // all anchor legs are 6-dec USD stables
  const netStableInBySymbol = {};
  for (const [sym, v] of bySymbol.entries()) {
    netStableInBySymbol[sym] = { formatted: formatUnits(v, 6), raw: v.toString() };
  }

  return {
    perPool,
    netStableInUSDC: formatUnits(usdc, 6),
    netStableInUSDCRaw: usdc.toString(),
    netStableInUSDT: formatUnits(usdt, 6),
    netStableInUSDTRaw: usdt.toString(),
    netStableInTotal: formatUnits(total, 6),
    netStableInTotalRaw: total.toString(),
    netStableInBySymbol,
  };
}

